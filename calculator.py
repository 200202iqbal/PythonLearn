def addtion(self,a=0,b=0):
    self.a = a
    self.b = b
    return self.a+self.b

def subtraction(self,a=0,b=0):
    self.a = a
    self.b = b
    return self.a-self.b

def multiplication(self,a=0,b=0):
    self.a = a
    self.b = b
    return self.a*self.b

def division(self,a=0,b=0):
    self.a = a
    self.b = b
    return self.a/self.b

def power(self,a=0,b=0):
    self.a = a
    self.b = b
    return self.a**self.b

